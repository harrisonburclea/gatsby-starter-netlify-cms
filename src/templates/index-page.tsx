import { graphql, Link } from 'gatsby'
import React, { FunctionComponent } from 'react'
import BlogRoll from '../components/BlogRoll'
import Features from '../components/Features'
import HeroAngleImage from '../components/HeroAngleImage';
import IconFeature from '../components/IconFeature';

import Layout from '../components/Layout'
import { IDict } from '../interfaces/dict.interface';
import { IQueryMarkdownData } from '../interfaces/page-data.interface';

interface IndexPageTemplateProps {
  image: IDict<any> | string;
  title: string;
  heading: string;
  subheading: string;
  mainpitch: IDict<any>;
  description: string;
  intro: {
    blurbs: Array<{
      image: IDict<any> | string;
      text: string
    }>;
  };
}

export const IndexPageTemplate: FunctionComponent<IndexPageTemplateProps> = (
  {
    image,
    title,
    heading,
    subheading,
    mainpitch,
    description,
    intro,
  }
) => (
  <div>
    <HeroAngleImage />
    <IconFeature />
    <section className="section section--gradient">
      <div className="container">
        <div className="section">
          <div className="columns">
            <div className="column is-10 is-offset-1">
              <div className="content">
                <div className="content">
                  <div className="tile">
                    <h1 className="title">{mainpitch.title}</h1>
                  </div>
                  <div className="tile">
                    <h3 className="subtitle">{mainpitch.description}</h3>
                  </div>
                </div>
                <div className="columns">
                  <div className="column is-12">
                    <h3 className="has-text-weight-semibold is-size-2">
                      {heading}
                    </h3>
                    <p>{description}</p>
                  </div>
                </div>
                <Features gridItems={intro.blurbs}/>
                <div className="columns">
                  <div className="column is-12 has-text-centered">
                    <Link className="btn" to="/products">
                      See all products
                    </Link>
                  </div>
                </div>
                <div className="column is-12">
                  <h3 className="has-text-weight-semibold is-size-2">
                    Latest stories
                  </h3>
                  <BlogRoll/>
                  <div className="column is-12 has-text-centered">
                    <Link className="btn" to="/blog">
                      Read more
                    </Link>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
    </section>
  </div>
);

const IndexPage: FunctionComponent<IQueryMarkdownData<{}>> = ({ data }) => {
  const { frontmatter } = data.markdownRemark;

  return (
    <Layout>
      <IndexPageTemplate
        image={frontmatter.image}
        title={frontmatter.title}
        heading={frontmatter.heading}
        subheading={frontmatter.subheading}
        mainpitch={frontmatter.mainpitch}
        description={frontmatter.description}
        intro={frontmatter.intro}
      />
    </Layout>
  )
};

export default IndexPage

export const pageQuery = graphql`
  query IndexPageTemplate {
    markdownRemark(frontmatter: { templateKey: { eq: "index-page" } }) {
      frontmatter {
        title
        image {
          childImageSharp {
            fluid(maxWidth: 2048, quality: 100) {
              ...GatsbyImageSharpFluid
            }
          }
        }
        heading
        subheading
        mainpitch {
          title
          description
        }
        description
        intro {
          blurbs {
            image {
              childImageSharp {
                fluid(maxWidth: 240, quality: 64) {
                  ...GatsbyImageSharpFluid
                }
              }
            }
            text
          }
          heading
          description
        }
      }
    }
  }
`;
